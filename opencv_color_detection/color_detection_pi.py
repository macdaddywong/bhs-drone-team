import cv2
import numpy as np
import argparse
import sys

# ──────────────────────────────────────────────
# HSV Color Ranges
# Hue: 0-179, Saturation: 0-255, Value: 0-255
# ──────────────────────────────────────────────
COLOR_RANGES = {
    "red": [
        (np.array([0,   120,  70]),  np.array([10,  255, 255])),
        (np.array([170, 120,  70]),  np.array([180, 255, 255])),   # red wraps around 180
    ],
    "orange": [
        (np.array([10,  120,  70]),  np.array([25,  255, 255])),
    ],
    "yellow": [
        (np.array([25,  100,  70]),  np.array([35,  255, 255])),
    ],
    "green": [
        (np.array([36,  100,  70]),  np.array([86,  255, 255])),
    ],
    "blue": [
        (np.array([94,  100,  70]),  np.array([126, 255, 255])),
    ],
    "purple": [
        (np.array([127, 50,   70]),  np.array([155, 255, 255])),
    ],
    "white": [
        (np.array([0,   0,   200]),  np.array([180,  30, 255])),
    ],
    "black": [
        (np.array([0,   0,    0]),   np.array([180,  255, 50])),
    ],
}

# BGR display colors for bounding boxes / labels
DISPLAY_COLORS = {
    "red":    (0,   0,   220),
    "orange": (0,   140, 255),
    "yellow": (0,   220, 220),
    "green":  (0,   200, 60),
    "blue":   (220, 80,  0),
    "purple": (180, 0,   180),
    "white":  (230, 230, 230),
    "black":  (60,  60,  60),
}

MIN_CONTOUR_AREA = 1500   # ignore tiny blobs (px²)
FRAME_WIDTH      = 640
FRAME_HEIGHT     = 480


