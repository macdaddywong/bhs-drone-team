def run(use_picam: bool = False, initial_color: str = None):
    all_colors    = list(COLOR_RANGES.keys())
    active_colors = [initial_color] if initial_color else list(all_colors)

    source = open_pi_camera() if use_picam else open_usb_camera()

    print("\n[INFO] Starting color detection. Press 'q' to quit.\n")
    print("  Keys 1-8 toggle individual colors:")
    for i, name in enumerate(all_colors):
        print(f"    {i+1}  →  {name}")
    print("  a  →  enable all colors")
    print("  n  →  disable all colors\n")

    while True:
        frame  = read_frame(source, use_picam)
        output = detect_and_annotate(frame, active_colors)
        output = draw_hud(output, active_colors, all_colors)

        cv2.imshow("Color Detection  —  Pi 4B", output)

        key = cv2.waitKey(1) & 0xFF

        if key == ord('q'):
            break
        elif key == ord('a'):
            active_colors = list(all_colors)
        elif key == ord('n'):
            active_colors = []
        elif ord('1') <= key <= ord('8'):
            idx  = key - ord('1')
            name = all_colors[idx]
            if name in active_colors:
                active_colors.remove(name)
                print(f"[INFO] Disabled: {name}")
            else:
                active_colors.append(name)
                print(f"[INFO] Enabled:  {name}")

    # Clean up
    cv2.destroyAllWindows()
    if use_picam:
        source.stop()
    else:
        source.release()
    print("[INFO] Done.")


