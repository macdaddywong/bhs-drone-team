def draw_hud(frame: np.ndarray, active_colors: list, all_colors: list) -> np.ndarray:
    """Draw a semi-transparent HUD showing active / available colors and key bindings."""
    overlay = frame.copy()
    h, w    = frame.shape[:2]

    # Bottom bar background
    cv2.rectangle(overlay, (0, h - 34), (w, h), (30, 30, 30), -1)
    cv2.addWeighted(overlay, 0.6, frame, 0.4, 0, frame)

    instructions = "Keys:  [1-8] toggle color   [a] all   [n] none   [q] quit"
    cv2.putText(frame, instructions, (8, h - 10),
                cv2.FONT_HERSHEY_SIMPLEX, 0.45, (200, 200, 200), 1, cv2.LINE_AA)

    # Color pills at top
    pill_x = 8
    for i, name in enumerate(all_colors):
        active = name in active_colors
        bgr    = DISPLAY_COLORS[name] if active else (70, 70, 70)
        label  = f"{i+1}:{name}"
        (tw, _), _ = cv2.getTextSize(label, cv2.FONT_HERSHEY_SIMPLEX, 0.42, 1)
        cv2.rectangle(frame, (pill_x, 6), (pill_x + tw + 8, 24), bgr, -1 if active else 1)
        txt_c = (0, 0, 0) if (active and name not in ("black", "blue", "purple")) else (220, 220, 220)
        cv2.putText(frame, label, (pill_x + 4, 20),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.42, txt_c, 1, cv2.LINE_AA)
        pill_x += tw + 14

    return frame


