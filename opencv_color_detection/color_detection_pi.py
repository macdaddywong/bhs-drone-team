def build_mask(hsv: np.ndarray, color_name: str) -> np.ndarray:
    """Return a combined binary mask for all HSV ranges of a color."""
    mask = np.zeros(hsv.shape[:2], dtype=np.uint8)
    for (lo, hi) in COLOR_RANGES[color_name]:
        mask |= cv2.inRange(hsv, lo, hi)
    # Morphological clean-up: remove noise, fill holes
    kernel = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (7, 7))
    mask = cv2.morphologyEx(mask, cv2.MORPH_OPEN,  kernel, iterations=2)
    mask = cv2.morphologyEx(mask, cv2.MORPH_CLOSE, kernel, iterations=2)
    return mask


def detect_and_annotate(frame: np.ndarray, active_colors: list) -> np.ndarray:
    """
    For each active color, find contours in the HSV mask and draw
    bounding boxes + labels on the original frame.
    """
    hsv    = cv2.cvtColor(frame, cv2.COLOR_BGR2HSV)
    output = frame.copy()

    for color_name in active_colors:
        mask      = build_mask(hsv, color_name)
        contours, _ = cv2.findContours(mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
        bgr       = DISPLAY_COLORS.get(color_name, (255, 255, 255))

        for cnt in contours:
            area = cv2.contourArea(cnt)
            if area < MIN_CONTOUR_AREA:
                continue

            x, y, w, h = cv2.boundingRect(cnt)
            cx = x + w // 2
            cy = y + h // 2

            # Bounding box
            cv2.rectangle(output, (x, y), (x + w, y + h), bgr, 2)

            # Label background
            label    = f"{color_name}  {int(area)}px"
            (tw, th), baseline = cv2.getTextSize(label, cv2.FONT_HERSHEY_SIMPLEX, 0.55, 1)
            cv2.rectangle(output, (x, y - th - baseline - 4), (x + tw + 4, y), bgr, -1)

            # Label text (black for readability on bright boxes)
            txt_color = (0, 0, 0) if color_name not in ("black", "blue", "purple") else (255, 255, 255)
            cv2.putText(output, label, (x + 2, y - baseline - 2),
                        cv2.FONT_HERSHEY_SIMPLEX, 0.55, txt_color, 1, cv2.LINE_AA)

            # Center dot
            cv2.circle(output, (cx, cy), 5, bgr, -1)

    return output


