def open_usb_camera(index: int = 0):
    cap = cv2.VideoCapture(index)
    if not cap.isOpened():
        sys.exit(f"[ERROR] Could not open USB camera at index {index}.")
    cap.set(cv2.CAP_PROP_FRAME_WIDTH,  FRAME_WIDTH)
    cap.set(cv2.CAP_PROP_FRAME_HEIGHT, FRAME_HEIGHT)
    cap.set(cv2.CAP_PROP_FPS, 30)
    print(f"[INFO] USB camera opened (index={index}).")
    return cap


def open_pi_camera():
    """Returns a picamera2 instance configured for OpenCV (BGR numpy arrays)."""
    try:
        from picamera2 import Picamera2  # type: ignore
    except ImportError:
        sys.exit(
            "[ERROR] picamera2 not found.\n"
            "Install with:  sudo apt install python3-picamera2\n"
            "Or use a USB webcam (omit --picam)."
        )
    picam = Picamera2()
    config = picam.create_preview_configuration(
        main={"size": (FRAME_WIDTH, FRAME_HEIGHT), "format": "BGR888"}
    )
    picam.configure(config)
    picam.start()
    print("[INFO] Pi Camera opened via picamera2.")
    return picam


def read_frame(source, use_picam: bool) -> np.ndarray:
    if use_picam:
        return source.capture_array()
    else:
        ret, frame = source.read()
        if not ret:
            sys.exit("[ERROR] Failed to grab frame from USB camera.")
        return frame


